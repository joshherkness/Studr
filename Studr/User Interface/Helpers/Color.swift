import UIKit
extension UIColor {
    static func blue() -> UIColor{
        return UIColor(hexString: "#3471D7")
    }
    
    static func red() -> UIColor{
        return UIColor(hexString: "#E44C5F")
    }
    
    static func green() -> UIColor{
        return UIColor(hexString: "#63D297")
    }
}